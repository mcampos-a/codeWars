// Given an array of integers, return a new array with each value doubled.

// For example:

// [1, 2, 3] --> [2, 4, 6]

//Solution I

function maps(x){
//map through the x array and make sure each element is num+num and return it. 
  return x.map(el => el+el)
}

//Parameters
//arr -> nums, it can be empty, it wont be null, and we will always get an arr

//Return
//arr -> each element num = num + num.

